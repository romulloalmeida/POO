package CheckpointI;

public class Telefone {
    private int numero;
    private int ddd;

    public Telefone(int numero, int ddd) {
        this.numero = numero;
        this.ddd = ddd;
    }

    public int getNumero() {
        return numero;
    }

    public int getDdd() {
        return ddd;
    }
}
